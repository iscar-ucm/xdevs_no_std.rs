use crate::{processor::Processor, simulator::Config, traits::sealedkind::SealedKind, CoupledKind};
use core::future::Future;
use sealed::Sealed;

#[cfg(any(feature = "embassy", feature = "std"))]
pub use crate::rt_engine::traits::{
    EjectOutput, InjectInput, RtEngineInputChannel, RtEngineOutputChannel,
};

/// Trait that defines the methods that a DEVS event bag set must implement.
///
/// # Safety
///
/// This trait must be implemented via macros. Do not implement it manually.
pub unsafe trait Bag {
    /// Build a new instance of the bag.
    fn build() -> Self;

    /// Returns `true` if the ports are empty.
    fn is_empty(&self) -> bool;

    /// Clears the ports, removing all values.
    fn clear(&mut self);
}

/// Trait that defines the type inside of a Bag for rt_engine enums.
///
/// # Safety
///
/// This trait is implemented internally. Do not implement it manually.
pub trait AsPort: Bag + Sealed {
    /// The type of the values contained in the bag.
    type Item;
}

/// Trait that defines a type that maps to event bag ports.
/// Its main purpose is its usage by the `RtEngine` to inject and eject events from the model's ports.
///
/// # Safety
///
/// This trait must be implemented via macros. Do not implement it manually.
pub unsafe trait BagMux: Bag {
    /// The type that represents the ports of the model. Each variant corresponds to a port.
    type Mux;

    /// Maps the type to the corresponding port, allowing to inject events to the bag.
    fn inject_event(&mut self, event: Self::Mux) -> Result<(), Self::Mux>;

    /// Maps the type to the corresponding port, allowing to receive events from the bag.
    fn eject_events(&self, ejector: impl FnMut(Self::Mux));
}

/// Interface for DEVS components. All DEVS components must implement this trait.
pub trait Component {
    /// Kind of DEVS model.
    type Kind: SealedKind;

    /// Input event bag of the model.
    type Input: Bag;

    /// Output event bag of the model.
    type Output: Bag;
}

/// Partial interface for DEVS coupled models.
/// It is used as a helper trait to implement [`crate::Coupled`] trait.
///
/// # Safety
///
/// This trait must be implemented via macros. Do not implement it manually.
pub unsafe trait PartialCoupled: Component<Kind = CoupledKind>
where
    Self::Components: AsProcessor<Input = Self::ComponentsInput, Output = Self::ComponentsOutput>,
{
    type Components: AsProcessor;
    type ComponentsInput: Bag;
    type ComponentsOutput: Bag;

    fn components(&mut self) -> &mut Self::Components;
    fn inputs(&mut self) -> &mut Self::ComponentsInput;
    fn outputs(&mut self) -> &mut Self::ComponentsOutput;
    fn split(
        &mut self,
    ) -> (
        &mut Self::Components,
        &mut Self::ComponentsInput,
        &mut Self::ComponentsOutput,
    );
}

/// Interface for simulating DEVS models. All DEVS models must implement this trait.
///
/// # Safety
///
/// This trait must be implemented via macros. Do not implement it manually.
pub unsafe trait AbstractSimulator<K>: Component<Kind = K>
where
    Self: Sized,
{
    /// It starts the simulation, setting the initial time to t_start.
    /// It returns the time for the next state transition of the inner DEVS model.
    fn start(simulator: &mut Processor<Self>, t_start: f64) -> f64;

    /// It stops the simulation, setting the last time to t_stop.
    fn stop(simulator: &mut Processor<Self>);

    /// Executes output functions and propagates messages according to EOCs.
    /// Internally, it checks that the model is imminent before executing.
    fn lambda(simulator: &mut Processor<Self>, output: &mut Self::Output, t: f64);

    /// Propagates messages according to ICs and EICs, and executes state transition functions.
    /// Internally, it checks that the model is imminent before executing.
    /// Finally, it returns the time for the next state transition of the inner DEVS model.
    fn delta(
        simulator: &mut Processor<Self>,
        input: &mut Self::Input,
        output: &mut Self::Output,
        t: f64,
    ) -> f64;
}

/// Interface for handling collections of DEVS models.
///
/// # Safety
///
/// This trait must be implemented via macros. Do not implement it manually.
pub unsafe trait AsProcessor: Component {
    fn starts(&mut self, t_start: f64) -> f64;
    fn stops(&mut self);
    fn lambdas(&mut self, output: &mut Self::Output, t: f64);
    fn deltas(&mut self, input: &mut Self::Input, output: &mut Self::Output, t: f64) -> f64;
}

/// Interface for handling input events in an asynchronous DEVS simulation.
///
/// Unlike other traits, this trait must be implemented by the user, as it is not generated by macros.
/// It allows the model to handle input events asynchronously, waiting for external events without blocking the simulation.
pub trait AsyncInput {
    /// Set this to the input event bag type of your model under study.
    type Input: Bag;

    /// Handles input events asynchronously.
    ///
    /// It receives the time interval `[t_from, t_until]` and a mutable reference to the input event bag.
    /// It returns the time of the next event, which is usually the time of the next state transition.
    /// If an external event occurs, it should inject the event to the input and return the time at which the event happened.
    fn handle(
        &mut self,
        config: &Config,
        t_from: f64,
        t_until: f64,
        input: &mut Self::Input,
    ) -> impl Future<Output = f64>;
}

pub(crate) mod sealed {
    /// Trait used to prevent users from implementing certain traits manually.
    pub trait Sealed {}
}

pub(crate) mod sealedkind {
    /// Trait used to prevent users from implementing certain traits manually.
    pub trait SealedKind {}
}
